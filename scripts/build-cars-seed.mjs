import fs from 'node:fs'
import path from 'node:path'

const projectRoot = process.cwd()
const csvPath = path.resolve(projectRoot, '../../tmp/ims-api-probe-20260408/ims_with_partner_imageurl.csv')
const optionsPath = path.resolve(projectRoot, '../../tmp/ims-api-probe-20260408/ims_car_options_by_car.json')
const outputPath = path.resolve(projectRoot, 'supabase/seed.sql')

function parseCsv(text) {
  const rows = []
  let row = []
  let field = ''
  let inQuotes = false

  for (let i = 0; i < text.length; i += 1) {
    const char = text[i]
    const next = text[i + 1]

    if (inQuotes) {
      if (char === '"' && next === '"') {
        field += '"'
        i += 1
      } else if (char === '"') {
        inQuotes = false
      } else {
        field += char
      }
      continue
    }

    if (char === '"') {
      inQuotes = true
      continue
    }

    if (char === ',') {
      row.push(field)
      field = ''
      continue
    }

    if (char === '\n') {
      row.push(field)
      rows.push(row)
      row = []
      field = ''
      continue
    }

    if (char === '\r') continue

    field += char
  }

  if (field.length > 0 || row.length > 0) {
    row.push(field)
    rows.push(row)
  }

  const [header, ...body] = rows
  return body.filter((r) => r.some(Boolean)).map((r) => Object.fromEntries(header.map((key, index) => [key, r[index] ?? ''])))
}

function cleanText(value) {
  const next = String(value || '').trim()
  return next || null
}

function toInt(value) {
  const n = Number(String(value || '').trim())
  return Number.isFinite(n) ? Math.trunc(n) : null
}

function sqlText(value) {
  if (value == null) return 'null'
  return `'${String(value).replace(/'/g, "''")}'`
}

function sqlBool(value) {
  return value ? 'true' : 'false'
}

function sqlJson(value) {
  return `${sqlText(JSON.stringify(value))}::jsonb`
}

const csvRaw = fs.readFileSync(csvPath, 'utf8')
const rows = parseCsv(csvRaw)
const optionPayload = JSON.parse(fs.readFileSync(optionsPath, 'utf8'))
const optionMap = new Map((optionPayload.rows || []).map((row) => [row.source_car_id, {
  ids: Array.isArray(row.option_ids) ? row.option_ids : [],
  names: Array.isArray(row.option_names) ? row.option_names : [],
  other: row.other_option ?? null,
}]))

const mapped = rows.map((row) => ({
  source_car_id: toInt(row.car_id),
  source_group_id: toInt(row.car_group_id),
  car_number: cleanText(row.car_identity),
  name: cleanText(row.partner_displayName) || cleanText(row.car_group_name) || cleanText(row.car_name) || '이름없음',
  display_name: cleanText(row.car_name),
  image_url: cleanText(row.partner_imageUrl),
  model_year: toInt(row.model_year),
  fuel_type: cleanText(row.oil_type),
  seats: (() => {
    const raw = row.raw_json ? JSON.parse(row.raw_json) : null
    return toInt(raw?.seater)
  })(),
  color: (() => {
    const raw = row.raw_json ? JSON.parse(row.raw_json) : null
    return cleanText(raw?.color)
  })(),
  rent_age: toInt(row.insurance_age),
  active: true,
  options_json: optionMap.get(toInt(row.car_id)) || { ids: [], names: [], other: null },
  metadata: {
    partner_car_id: toInt(row.partner_carId),
    partner_list_name: cleanText(row.partner_listName),
    partner_detail_name: cleanText(row.partner_detailName),
    image_joined: row.image_joined === 'true',
    source: 'ims-api-probe-20260408',
  },
})).filter((row) => row.source_car_id && row.car_number)

const sql = `-- generated by scripts/build-cars-seed.mjs\n` +
`truncate table public.cars restart identity;\n\n` +
`insert into public.cars (\n  source_car_id,\n  source_group_id,\n  car_number,\n  name,\n  display_name,\n  image_url,\n  model_year,\n  fuel_type,\n  seats,\n  color,\n  rent_age,\n  active,\n  options_json,\n  metadata\n) values\n` +
 mapped.map((row) => `  (${[
   row.source_car_id,
   row.source_group_id ?? 'null',
   sqlText(row.car_number),
   sqlText(row.name),
   sqlText(row.display_name),
   sqlText(row.image_url),
   row.model_year ?? 'null',
   sqlText(row.fuel_type),
   row.seats ?? 'null',
   sqlText(row.color),
   row.rent_age ?? 'null',
   sqlBool(row.active),
   sqlJson(row.options_json),
   sqlJson(row.metadata),
 ].join(', ')})`).join(',\n') +
`\non conflict (source_car_id) do update set\n  source_group_id = excluded.source_group_id,\n  car_number = excluded.car_number,\n  name = excluded.name,\n  display_name = excluded.display_name,\n  image_url = excluded.image_url,\n  model_year = excluded.model_year,\n  fuel_type = excluded.fuel_type,\n  seats = excluded.seats,\n  color = excluded.color,\n  rent_age = excluded.rent_age,\n  active = excluded.active,\n  options_json = excluded.options_json,\n  metadata = excluded.metadata,\n  updated_at = now();\n`

fs.mkdirSync(path.dirname(outputPath), { recursive: true })
fs.writeFileSync(outputPath, sql)

console.log(JSON.stringify({
  ok: true,
  outputPath,
  rows: mapped.length,
  withImage: mapped.filter((row) => row.image_url).length,
}, null, 2))
